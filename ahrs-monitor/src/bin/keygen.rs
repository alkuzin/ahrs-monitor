//! Keys & firmware config generating tool for AHRS Monitor.

use ahrs_monitor::config::{self, AppConfig};
use chrono::Local;
use rand::Rng;
use std::{fs, path::Path};
use indtp::types::{AesKey, HmacKey};

/// Path for generated keys.
const SECRETS_DIR: &str = "configs/firmware/secrets";

/// Path for generated AES-128 key.
const AES_KEY_PATH: &str = "configs/firmware/secrets/aes.key";

/// Path for generated HMAC key.
const HMAC_KEY_PATH: &str = "configs/firmware/secrets/hmac.key";

/// Path for generated firmware configs.
const FIRMWARE_DIR: &str = "configs/firmware";

/// Path for generated firmware config for STM32.
const STM32_CONFIG_PATH: &str = "configs/firmware/stm32_config.rs";

/// Path for generated firmware config for ESP32.
const ESP32_CONFIG_PATH: &str = "configs/firmware/esp32_config.rs";

/// Relative path for generated AES-128 key.
const AES_KEY_RELATIVE_PATH: &str = "../../configs/firmware/secrets/aes.key";

/// Relative path for generated HMAC key.
const HMAC_KEY_RELATIVE_PATH: &str = "../../configs/firmware/secrets/hmac.key";

/// Generate AES-128 & HMAC-SHA256 keys.
///
/// # Returns
/// - `Ok` - in case of success.
/// - `Err` - otherwise.
fn generate_keys() -> anyhow::Result<()> {
    let dir = Path::new(SECRETS_DIR);
    fs::create_dir_all(dir).expect("Failed to create secrets directory");

    // Generating new keys.
    let mut aes_key: AesKey = Default::default();
    let mut hmac_key: HmacKey = Default::default();
    let mut rng = rand::rng();

    rng.fill_bytes(&mut aes_key);
    rng.fill_bytes(&mut hmac_key);

    // Rewriting old keys.
    fs::write(&AES_KEY_PATH, aes_key)?;
    fs::write(&HMAC_KEY_PATH, hmac_key)?;

    println!("[+] Keys successfully updated: {dir:?}");
    Ok(())
}

/// Generate config file for STM32 firmware.
///
/// # Parameters
/// - `cfg` - given application's configurations to handle.
///
/// # Returns
/// - `Ok` - in case of success.
/// - `Err` - otherwise.
fn generate_stm32_config(cfg: &AppConfig) -> anyhow::Result<()> {
    let dir = Path::new(FIRMWARE_DIR);
    fs::create_dir_all(dir)?;

    let now = Local::now().format("%Y-%m-%d %H:%M:%S");
    let content = format!(
        r#"// SPDX-License-Identifier: Apache-2.0
// Copyright (C) 2026-present ahrs-monitor project and contributors.

//! STM32 firmware configuration.
//! AUTO-GENERATED by AHRS Monitor keygen tool.
//! DO NOT EDIT MANUALLY. Use AHRS Monitor config and re-run generator.
//! Generated: {now}

/// IMU sample rate in Hz.
pub const SAMPLE_RATE_HZ: u16 = {};

/// IMU device identifier.
pub const DEVICE_ID: u8 = {};

/// INDTP protocol operating mode.
pub const PROTOCOL_MODE: u8 = {};
"#,
        cfg.imu.sample_rate,
        cfg.imu.device_id,
        cfg.imu.protocol_mode,
    );

    fs::write(Path::new(STM32_CONFIG_PATH), content)?;
    println!("[+] STM32 config generated: {STM32_CONFIG_PATH}");
    Ok(())
}

/// Generate config file for ESP32 firmware (WITH KEYS via include_bytes!).
///
/// # Parameters
/// - `cfg` - given application's configurations to handle.
///
/// # Returns
/// - `Ok` - in case of success.
/// - `Err` - otherwise.
fn generate_esp32_config(cfg: &AppConfig) -> anyhow::Result<()> {
    let dir = Path::new(FIRMWARE_DIR);
    fs::create_dir_all(dir)?;

    let now = Local::now().format("%Y-%m-%d %H:%M:%S");
    let content = format!(
        r#"// SPDX-License-Identifier: Apache-2.0
// Copyright (C) 2026-present ahrs-monitor project and contributors.

//! ESP32 firmware configuration.
//! AUTO-GENERATED by AHRS Monitor keygen tool.
//! DO NOT EDIT MANUALLY. Use AHRS Monitor config and re-run generator.
//! Generated: {now}

/// AHRS Monitor ingester IP address.
pub const MONITOR_IP: &str = "{}";

/// AHRS Monitor ingester UDP port.
pub const MONITOR_PORT: u16 = {};

/// Whether to encrypt the frame payload.
pub const USE_ENCRYPTION: bool = {};

/// AES-128 encryption key.
pub const AES_KEY: &[u8; 16] = include_bytes!("{AES_KEY_RELATIVE_PATH}");

/// HMAC-SHA256 authentication key.
pub const HMAC_KEY: &[u8; 32] = include_bytes!("{HMAC_KEY_RELATIVE_PATH}");
"#,
        cfg.net.ip_address,
        cfg.net.udp_port,
        cfg.net.use_encryption,
    );

    fs::write(Path::new(ESP32_CONFIG_PATH), content)?;
    println!("[+] ESP32 config generated: {ESP32_CONFIG_PATH}");
    Ok(())
}

/// Generate all firmware configs based on AHRS Monitor config.
///
/// # Returns
/// - `Ok` - in case of success.
/// - `Err` - otherwise.
fn generate_configs() -> anyhow::Result<()> {
    let app_config = config::load_config(config::CONFIG_FILE_PATH)?;

    generate_stm32_config(&app_config)?;
    generate_esp32_config(&app_config)?;
    Ok(())
}

/// Run keys & firmware config generator.
///
/// # Returns
/// - `Ok` - in case of success.
/// - `Err` - otherwise.
fn run() -> anyhow::Result<()> {
    println!("(*) Generating cryptographic keys...");
    generate_keys()?;

    println!("\n(*) Generating firmware configurations...");
    generate_configs()?;
    Ok(())
}

fn main() {
    println!("AHRS Monitor — Firmware Keys & Config Generator");
    println!("-----------------------------------------------\n");

    if let Err(e) = run() {
        eprintln!("Error occurred during generation: {e}");
    }

    println!("\nDone");
}
